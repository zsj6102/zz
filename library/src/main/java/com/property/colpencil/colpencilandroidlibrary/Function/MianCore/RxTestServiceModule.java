public class RxTestServiceModule {
    //baserl
    private static final String BASETESTURL = "http://120.24.167.226:8080/qqjx/app/find/";
    //配置ohhttp
    private static OkHttpClient okHttpClient = new OkHttpClient.Builder().addInterceptor
            (new HttpLoggingInterceptor().setLevel(HttpLoggingInterceptor.Level.BODY)).build();
    //配置初始化Retrofit
    private static Retrofit retrofit = new Retrofit.Builder()
            .baseUrl(BASETESTURL)
            .client(okHttpClient)
            .addCallAdapterFactory(RxJavaCallAdapterFactory.create())
            .addConverterFactory(GsonConverterFactory.create())
            .build();


    //保证RxService为单例的
    private RxTestServiceModule() {


    }

    //创建Api
    public static <T> T createApi(Class<T> clazz) {

        return retrofit.create(clazz);
    }
}
